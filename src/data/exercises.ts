export const diagnosticPool: string[] = exercises
  .filter(e => e.isDiagnostic)
  .map(e => e.id);

export const getExerciseById = (id: string): Exercise | undefined =>
  exercises.find(e => e.id === id);

export const getExercisesBySkill = (skill: string): Exercise[] =>
  exercises.filter(e => e.skill === skill);

export const getExercisesByLevel = (level: string): Exercise[] =>
  exercises.filter(e => e.cefrLevel === level);

export const getObjectiveExercises = (): Exercise[] =>
  exercises.filter(e =>
    ['multipleChoice', 'cloze', 'connectorChoice', 'collocationChoice', 'accentPractice', 'readingQuestion', 'listeningLikePrompt'].includes(e.type)
  );

export const getSubjectiveExercises = (): Exercise[] =>
  exercises.filter(e =>
    ['writingPrompt', 'speakingPrompt', 'registerRewrite', 'sentenceTransformation'].includes(e.type)
  );
